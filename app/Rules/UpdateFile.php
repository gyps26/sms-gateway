namespace App\Rules;

use App\Zip;
use Closure;
use Illuminate\Contracts\Validation\ValidationRule;

class UpdateFile implements ValidationRule
{
    /**
     * Run the validation rule.
     *
     * @param  \Closure(string, ?string=): \Illuminate\Translation\PotentiallyTranslatedString  $fail
     */
    public function validate(string $attribute, mixed $value, Closure $fail): void
    {
        $requiredFiles = [
            'artisan'
        ];

        $files = Zip::index($value);

        if (count(array_intersect($requiredFiles, $files)) === count($requiredFiles)) {
            return;
        }

        $fail(__('validation.custom.update_file'));
    }
}
