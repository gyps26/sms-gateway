namespace App\SignatureValidators;

use Illuminate\Http\Request;
use Illuminate\Support\Str;
use Spatie\WebhookClient\Exceptions\InvalidConfig;
use Spatie\WebhookClient\SignatureValidator\SignatureValidator;
use Spatie\WebhookClient\WebhookConfig;

class CryptoComSignatureValidator implements SignatureValidator
{

    /**
     * @throws \Spatie\WebhookClient\Exceptions\InvalidConfig
     */
    public function isValid(Request $request, WebhookConfig $config): bool
    {
        $signature = $request->header($config->signatureHeaderName);

        if (! $signature) {
            return false;
        }

        $parts = explode(',', $signature);
        $timestamp = Str::after($parts[0], 't=');
        $signature = Str::after($parts[1], 'v1=');

        $signingSecret = $config->signingSecret;

        if (empty($signingSecret)) {
            throw InvalidConfig::signingSecretNotSet();
        }

        $computedSignature = hash_hmac('sha256', $timestamp . '.' . $request->getContent(), $signingSecret);

        return hash_equals($signature, $computedSignature);
    }
}
